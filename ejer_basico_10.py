##programa para poder hallar el perimetro ya area de un circulo
diametro= int(input("por favor digite el diametro del circulo: " ))
radio= (diametro/2)
perimetro= (2 * 3.14 * radio)
area= (3.14 * radio**2)
print("el resultado del perimetro del circulo es:", perimetro)
print("el resultado del calculo del area del circulo es:", area)